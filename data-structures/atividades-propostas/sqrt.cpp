#include <iostream>
#include <cmath>

using namespace std;

int main() {

  double a, b, c;

  a=4;
  b=12.25;
  c=0.0121;
  
  cout << "Raiz quadrada de 4: " << sqrt(a) << endl;
  cout << "Raiz quadrada de 12.25: " << sqrt(b) << endl;
  cout << "Raiz quadrada de 0.0121: " << sqrt(c) << endl;
  
  return 0;
}